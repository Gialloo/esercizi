public class ProvaSwitch{
	

	public static void main(String[] args) {
		
		int mese=5;

		switch(mese)
		{
			case 1:
				System.out.println("Gennaio");
				break;
			case 2:
				System.out.println("Febbraio");
				break;
			case 3:
				System.out.println("Marzo");
				break;
			case 4:
				System.out.println("Aprile");
				break;

				default:
					System.out.println("numero non valido");

		}
		
		


	}

}