public class CicloWhile{
	
	public static void main(String[] args) {
		

		// ciclo while, prima forma
		int cont=0;

		while(cont<=5)
		{

				System.out.println(cont);

				cont++;
		}

		//ciclo do while

		int i=5;
		do
		{

				System.out.println(i);

				i++;

		}
		while(i<5);
	}

}